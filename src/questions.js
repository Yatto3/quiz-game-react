    shuffleElements(){
        for (let i = this.questions.length - 1  ; i >= 0 ; i--){
          
            let rand_indexQ = Math.floor(Math.random() * this.questions.length) ;
            let temp_index = this.questions[i];
            let temp_index2 = this.answers[i];
                
            this.questions[i] = this.questions[rand_indexQ];
            this.answers[i] = this.answers[rand_indexQ];
    
            this.questions[rand_indexQ] = temp_index ;
            this.answers[rand_indexQ] = temp_index2;

        }
       
    }

   
